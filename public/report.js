// report.js — public GDPR compliance report page

const CIRC = 2 * Math.PI * 42; // SVG arc circumference

function scoreColor(s) {
  if (s < 40) return 'var(--bad)';
  if (s < 65) return 'var(--warn)';
  return 'var(--good)';
}

function riskInfo(s) {
  if (s < 40) return ['HIGH RISK',      'risk--high'];
  if (s < 65) return ['MODERATE RISK',  'risk--medium'];
  if (s < 80) return ['LOW RISK',       'risk--low'];
  return              ['COMPLIANT',      'risk--clean'];
}

function sevClass(sev) {
  return { critical: 'sev--critical', warning: 'sev--warning', good: 'sev--good', info: 'sev--info' }[sev] || 'sev--info';
}

function esc(s) {
  return String(s ?? '')
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}

function fmtDate(iso) {
  if (!iso) return '—';
  return new Date(iso).toLocaleDateString('en-GB', { day: 'numeric', month: 'long', year: 'numeric' });
}

function render(data) {
  const score    = data.score ?? 0;
  const offset   = CIRC * (1 - score / 100);
  const color    = scoreColor(score);
  const [riskTxt, riskCls] = riskInfo(score);
  const trackers = (Array.isArray(data.trackers) ? data.trackers : []).map(t => t.name || t).filter(Boolean);
  const findings = Array.isArray(data.findings) ? data.findings : [];
  const critical = findings.filter(f => f.severity === 'critical').length;
  const summary  = critical
    ? `${critical} critical issue${critical !== 1 ? 's' : ''} detected on your store.`
    : `${findings.length} compliance issue${findings.length !== 1 ? 's' : ''} detected.`;

  document.title = `GDPR Report — ${data.domain} — Consentry`;

  const trackersHtml = trackers.length ? `
    <div class="section">
      <div class="section-label">
        <span class="section-label__text">Trackers firing before consent</span>
        <span class="section-label__count">${trackers.length} found</span>
      </div>
      <ul class="tracker-pills">
        ${trackers.map(n => `<li class="tracker-pill">${esc(n)}</li>`).join('')}
      </ul>
    </div>` : '';

  const findingsHtml = findings.length ? `
    <div class="section">
      <div class="section-label">
        <span class="section-label__text">Compliance issues</span>
        <span class="section-label__count">${findings.length} found</span>
      </div>
      ${findings.map(f => `
        <div class="finding">
          <span class="finding__sev ${sevClass(f.severity)}">${esc(f.severity || 'info')}</span>
          <div>
            <div class="finding__title">${esc(f.title)}</div>
            ${f.detail ? `<p class="finding__detail">${esc(f.detail)}</p>` : ''}
          </div>
        </div>`).join('')}
    </div>` : '';

  const email = esc(data.contactEmail || '');
  const mailto = `mailto:${data.contactEmail || ''}?subject=${encodeURIComponent('Fix checklist for ' + data.domain)}`;

  document.getElementById('root').innerHTML = `
    <header class="rpt-head">
      <a href="/"><img src="/logo.png" alt="Consentry" /></a>
      <span class="rpt-head__label">GDPR Compliance Report</span>
    </header>

    <div class="score-card">
      <svg class="gauge" viewBox="0 0 100 100" width="140" height="140" aria-hidden="true">
        <circle class="gauge__track" cx="50" cy="50" r="42" />
        <circle
          class="gauge__arc"
          cx="50" cy="50" r="42"
          stroke="${color}"
          stroke-dasharray="${CIRC.toFixed(1)}"
          stroke-dashoffset="${offset.toFixed(1)}"
        />
        <text class="gauge__num" x="50" y="47">${score}</text>
        <text class="gauge__sub" x="50" y="63">/100</text>
      </svg>

      <div class="score-meta">
        <p class="score-meta__eyebrow">${esc(data.domain)}</p>
        <h1 class="score-meta__heading">Compliance Report</h1>
        <span class="risk-badge ${riskCls}">${riskTxt}</span>
        <p class="score-meta__note">Scanned ${fmtDate(data.scanned_at)} · ${summary}</p>
      </div>
    </div>

    ${trackersHtml}
    ${findingsHtml}

    <div class="cta-box">
      <h2>Want the exact fix checklist for ${esc(data.domain)}?</h2>
      <p>Takes 5 minutes to read. Specific steps for your store's setup — not a generic guide. Could save you from a significant GDPR fine.</p>
      <a class="cta-btn" href="${mailto}">Request your fix checklist →</a>
    </div>

    <footer class="rpt-footer">
      <p>Generated by <a href="/">Consentry</a> · Free GDPR compliance scanner for Shopify stores.</p>
    </footer>
  `;
}

function showError(msg) {
  document.getElementById('root').innerHTML = `
    <div class="state">
      <h2>Report not found</h2>
      <p>${esc(msg)}</p>
      <p><a href="/">Run a free scan on your store →</a></p>
    </div>`;
}

async function init() {
  const domain = decodeURIComponent(window.location.pathname.replace(/^\/report\/?/, ''));
  if (!domain || !domain.includes('.')) { showError('No domain specified.'); return; }
  try {
    const res = await fetch(`/api/report/${encodeURIComponent(domain)}`);
    if (res.status === 404) { showError('This report link may have expired or the domain wasn\'t scanned.'); return; }
    if (!res.ok) { showError('Failed to load report. Please try again.'); return; }
    render(await res.json());
  } catch {
    showError('Network error — please try again.');
  }
}

init();
